#include "GFFT.h"
//===============================================
GFFT* GFFT::m_instance = 0;
//===============================================
GFFT::GFFT() {
    m_data = "_NONE_";
}
//===============================================
GFFT::~GFFT() {
    
}
//===============================================
GFFT* GFFT::Instance() {
    if(m_instance == 0) {
        m_instance = new GFFT;
    }
    return m_instance;
}
//===============================================
void GFFT::setData(const string& data) {
    m_data = data;
}
//===============================================
void GFFT::showData() const {
    cout << m_data << "\n";
}
